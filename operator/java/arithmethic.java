package operator.java;
public class arithmethic {
    public static void main(String[] args) {
        int num1=67;
        int num2=34;
        System.out.println("sum="+(num1+num2));
        System.out.println("sum="+(num1-num2));
        System.out.println("sum="+(num1*num2));
        System.out.println("sum="+(num1/num2));
        System.out.println("sum="+(num1%num2));
    }
}
