package basics;
public class updatingVariables {
    public static void main(String[] args) {
        int x=5;
        System.out.println(x);
        x=7;
        System.out.println(x);
        x=x+7;//x+=7 shortcut
        System.out.println(x);
        x=x-4;//x-=4
        System.out.println(x);
        x=x*23;//x*=23;
        System.out.println(x);
    }
}
